function matrixRows(matrix) {
  return matrix.rows.flatMap((row) =>
    row.cells
      .filter((cell) => cell?.ending)
      .map((cell, index) => ({
        ending: cell.ending,
        parse: `${row.label} / ${matrix.columns[index]}`,
        tag: cell.tag,
        examples: cell.examples,
        note: cell.note ?? `${row.label} 的 ${matrix.columns[index]} 形式。`,
      })),
  );
}

